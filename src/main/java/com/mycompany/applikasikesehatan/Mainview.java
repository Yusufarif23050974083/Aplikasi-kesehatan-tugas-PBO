    private void tombolsehatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolsehatActionPerformed
        // TODO add your handling code here:
        umur = Integer.parseInt(UMUR.getText());
        if ((umur > 0)&&(umur < 12) && (GENDER.getText().equals("LAKI-LAKI"))) {
            komentarsehat.setText("anda adalah anak laki-laki dibawah umur");
        } else if ((umur > 0) && (umur < 12) && (GENDER.getText().equals("PEREMPUAN"))) {
            komentarsehat.setText("anda adalah anak perempuan dibawah umur");
        } else if ((umur > 13) && (umur < 20) && (GENDER.getText().equals("LAKI-LAKI"))) {
            komentarsehat.setText("anda adalah anak remaja laki-laki");
        } else if ((umur > 13) && (umur < 20) && (GENDER.getText().equals("PEREMPUAN"))) {
            komentarsehat.setText("anda adalah orang remaja perempuan");
        } else if ((umur > 20) && (umur < 50) && (GENDER.getText().equals("PEREMPUAN"))) {
            komentarsehat.setText("anda adalah orang dewasa perempuan");
        } else if ((umur > 20) && (umur < 50) && (GENDER.getText().equals("LAKI-LAKI"))) {
            komentarsehat.setText("anda adalah orang dewasa laki-laki");
        } else if ((umur > 50) && (GENDER.getText().equals("PEREMPUAN"))) {
            komentarsehat.setText("anda adalah lansia perempuan");
        } else if ((umur > 50) && (GENDER.getText().equals("PEREMPUAN"))) {
            komentarsehat.setText("anda adalah lansia laki-laki");
        }
    }//GEN-LAST:event_tombolsehatActionPerformed

