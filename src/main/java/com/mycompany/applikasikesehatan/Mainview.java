    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        jamawalTidur = Integer.parseInt(awaltidur.getText());
        menitAwaltidur = Integer.parseInt(menitawaltidur.getText());
        jamawalBangun = Integer.parseInt(awalbangun.getText());
        menitAwalbangun = Integer.parseInt(menitawalbangun.getText());

        LocalTime jamTidur = LocalTime.of(jamawalTidur, menitAwaltidur);
        LocalTime jamBangun = LocalTime.of(jamawalBangun, menitAwalbangun);

        Duration duration = Duration.between(jamBangun, jamTidur);

        hours = duration.toHours();
        minutes = duration.toMinutesPart();
        hasilkurang.setText(hours+":"+minutes);
    }//GEN-LAST:event_jButton10ActionPerformed

